	<h4>Sexual Assualt Statistics</h4>
	<h5 class="addItalic2">General Sexual Violence</h5>
	<ul>
		<li>1 in 5 women and 1 in 71 men will be raped at some point in their lives.</li>

		<li>In 8 out of 10 cases of rape, the victim knew the person who sexually assaulted them.</li>

		<li>8% of rapes occur in the victim’s workplace.</li>
	</ul>
	
	<h5 class="addItalic2">Sexual Violence Against Minors</h5>
	<ul>
		<li>1 in four girls and 1 in 6 boys will be sexually abused before they turn 18 years old.</li>

		<li>34% of people who sexually abuse a child are family members.</li>

		<li>The average age at which girls first become victims of prostitution is 12 to 14 years old, and the average age for boys is 11 to 13 years old.</li>
	</ul>

	<h5 class="addItalic2">Sexual Violence on Campus</h5>
	<ul>
		<li>1 in 5 women and 1 in 16 men are sexually assaulted while in college.</li>

		<li>More than 90% of sexual assault victims on college campuses do not report the assault.</li>
	</ul>

	<h5 class="addItalic2">Crime Rates for Sexual Assault</h5>
	<ul>
		<li>Rape is the most under-reported crime; 63% of sexual assaults are not reported to police (o). Only 12% of child sexual abuse is reported to the authorities.</li>

		<li>The prevalence of false reporting is between 2% and 10%. For example, a study of eight U.S. communities, which included 2,059 cases of sexual assault, found a 7.1% rate of false reports (k). A study of 136 sexual assault cases in Boston found a 5.9% rate of false reports (j). Researchers studied 812 reports of sexual assault from 2000-03 and found a 2.1% rate of false reports (NSVRC 2012, 2013, 2015).</li>
	</ul>


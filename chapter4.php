	<h4>Child Behavior and Attachments</h4>
	<p>Even during childhood humans are studied to understand their nature. One area focused on by Mary Ainsworth &mdash;a Canadian-American Psychologist&mdash; is called Attachment Theory and relates to a child’s attachment type to its parents. The theory behind it goes into details saying how children during early stages of their lives learn how they should behave or be dependent on others, which then becomes the basis for their adult selves behavior attachments.</p>
	
	<p>The 4 main categories for child attachment are Secure, Avoidant, Ambivalent, Disorganized. The behaviors of both the child and caregiver for these attachments are given below.</p>

	<ul class="solidCircle">
		<li>Secure from Caregiver: The caregiver is responsive, quick and positive towards the child’s needs.</li>

		<li>Secure from Child: The child is Distressed when caregiver leaves, happy when caregiver returns, and seeks comfort from the caregiver when scared or sad.</li>	
	<!- Interesting Fact goes here ->

		<li>Avoidant from Caregiver: The caregiver is unresponsive, uncaring or dismissive towards the child.</li>

		<li>Avoidant from Child: The child does not acknowledge the caregiver arriving or leaving and does not seek or make contact with caregiver.</li>

		<li>Ambivalent from Caregiver: The caregiver responds inconsistently to the child.</li>

		<li>Ambivalent from Child: The child is distressed when the caregiver leaves but feels no comfort by the return of the caregiver.</li>

		<li>Disorganized from Caregiver: The caregiver is abusive or neglectful and often responds in frightening or frightened ways towards the child.</li>

		<li>Disorganized from Child: The child has no attaching behaviors, often appears dazed, confused, or apprehensive in presence of caregiver.</li>
	</ul>	
	
